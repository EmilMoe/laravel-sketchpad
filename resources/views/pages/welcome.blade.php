
<h3>Welcome</h3>

<p>Welcome to Laravel Sketchpad!</p>

<h3>What is this?</h3>

<p>Sketchpad is an interactive front-end for your back-end; a place to test, code, experiment, or just group useful tools and functions in one place.</p>

<p>It shows the controllers and methods from your <code>Controllers/{{ $route }}</code> folder, and allows you to run them without setting up routing, views, etc.</p>

<p>Add controllers and methods manually, or click <a class="command" href="/{{ $route }}:new/">New</a> to automate the process...</p>